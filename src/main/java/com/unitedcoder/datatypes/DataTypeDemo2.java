package com.unitedcoder.datatypes;



public class DataTypeDemo2 {
    public static void main(String[] args) {
        byte b1=Byte.MIN_VALUE;
        byte b2=Byte.MAX_VALUE;
        System.out.println("Minimum Value of Byte is: " +b1);
        System.out.println("Maximum Value of Byte is: " +b2);

        int i1=Integer.MIN_VALUE;
        int i2=Integer.MAX_VALUE;
        System.out.println("Minimum Value of Integer is: " +i1);
        System.out.println("Maximum Value of Integer is: " +i2);

        float f1=Float.MIN_VALUE;
        float f2=Float.MAX_VALUE;
        System.out.println("Minimum Value of Float is: " +f1);
        System.out.println("Maximum Value of Float is: " +f2);
    }
}
